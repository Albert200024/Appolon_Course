function replaceV2(text, a, b){
  let arrText = text.split("");
  let newArr = [];

  let x = 0;
  let y = 0;

  if(!text.includes(a)){
      return arrText.join("")
  } else {
    for(let i = 0; i < a.length; i++){
         if(i == 0) {
             x = text.indexOf(a[i]);
             y = text.indexOf(a[i]);
         }
         y++
    }

    for (let i = 0; i < text.length; i++) {
          if(i < x){
             newArr.push(text[i])
          }  else if(i >= x && x <= y){
              newArr.push(b);
              break
          }
    }

    for(let i = y; i < text.length; i++){
        newArr.push(text[i])
    }
  }

  return newArr.join("");
}

